def speak_to_grandma(string)
  case string
  when 'I LOVE YOU GRANDMA!'
    'I LOVE YOU TOO PUMPKIN!'
  when 'Hi Nana, how are you?'
    'HUH?! SPEAK UP, SONNY!'
  when 'Hi!'
    'HUH?! SPEAK UP, SONNY!'
  when 'WHAT DID YOU EAT TODAY?'
    'NO, NOT SINCE 1938!'
  when 'WHAT?'
    'NO, NOT SINCE 1938!'
  end
end

speak_to_grandma('I LOVE YOU GRANDMA!')